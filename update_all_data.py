def insert_restrictions(db):
    c = db.cursor()
    new_count = 0
    changed_count = 0
    seen = set()
    for reg, net, ct, lt, val in RESTRICTIONS:
        if not reg:
            continue
        net_fixed = net if net else ""
        key = (reg, net_fixed, ct, lt)
        if key in seen:
            continue
        seen.add(key)
        cur = c.execute(
            "SELECT id, limit_value FROM restrictions WHERE region=? AND network=? AND client_type=? AND limit_type=?",
            (reg, net_fixed, ct, lt)
        ).fetchone()
        if cur:
            if cur[1] != val:
                c.execute(
                    "UPDATE restrictions SET is_current=1, previous_value=limit_value, limit_value=?, updated_at=datetime('now') WHERE id=?",
                    (val, cur[0])
                )
                changed_count += 1
        else:
            c.execute(
                "INSERT OR IGNORE INTO restrictions(region,network,client_type,limit_type,limit_value,source_url,source_date) VALUES(?,?,?,?,?,?,?)",
                (reg, net_fixed, ct, lt, val, SR_RESTR_URL, "03.07.2026")
            )
            new_count += 1
    print(f"Restrictions: {new_count} new, {changed_count} changed")

def verify(db):
    c = db.cursor()
    prices = c.execute("SELECT COUNT(*) FROM prices").fetchone()[0]
    active = c.execute("SELECT COUNT(*) FROM restrictions WHERE is_current=1").fetchone()[0]
    total = c.execute("SELECT COUNT(*) FROM restrictions").fetchone()[0]
    changes = c.execute("SELECT COUNT(*) FROM restrictions WHERE previous_value IS NOT NULL").fetchone()[0]
    print(f"\n=== VERIFICATION ===")
    print(f"prices: {prices}")
    print(f"active restrictions: {active}")
    print(f"total restrictions: {total}")
    print(f"changes: {changes}")

